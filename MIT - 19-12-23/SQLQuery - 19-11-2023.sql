SELECT E2_PARCELA, E2_VENCREA, E2_VALOR, IIF(E2_BAIXA = '', CASE WHEN ISNUMERIC('R$ 0,00') = 0.00 THEN CAST(CAST(0.00 AS MONEY) AS FLOAT) END, SUM(E5_VALOR)) AS VLR_PAGO, E2_BAIXA 
FROM SE2990 AS SE2  
LEFT JOIN SE5990 AS SE5 ON 
	SE5.E5_FILIAL = '01' AND   
	SE2.E2_PREFIXO = SE5.E5_PREFIXO AND  
	SE2.E2_NUM = SE5.E5_NUMERO AND  
	SE2.E2_PARCELA = SE5.E5_PARCELA AND  
	SE2.E2_TIPO = SE5.E5_TIPO AND  
	SE2.E2_FORNECE = SE5.E5_FORNECE AND  
	SE2.E2_LOJA = SE5.E5_LOJA AND  
	SE5.D_E_L_E_T_ = ''  
INNER JOIN SF1990 AS SF1  ON 
	SF1.F1_FILIAL = '01' AND  
	SE2.E2_NUM = SF1.F1_DOC AND  
	SE2.E2_PREFIXO =  SF1.F1_SERIE AND  
	SE2.E2_FORNECE =  SF1.F1_FORNECE AND  
	SE2.E2_LOJA =  SF1.F1_LOJA AND  
	SF1.D_E_L_E_T_ = ''  
WHERE SF1.F1_DOC = '10589' AND SE5.E5_RECPAG = 'P' AND  
SE2.E2_FILIAL = '01' AND SE2.E2_PREFIXO = '01' 
AND SE2.E2_NUM = '88988' AND SE2.E2_PARCELA = '' 
AND SE2.E2_TIPO = 'NF'AND SE2.E2_FORNECE = '789585' 
AND SE2.E2_LOJA = '65' AND SE2.D_E_L_E_T_ = ''  
GROUP BY E2_PARCELA, E2_VENCREA, E2_VALOR, E2_BAIXA 
