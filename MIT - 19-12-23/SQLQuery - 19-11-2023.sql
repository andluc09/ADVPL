SELECT DISTINCT E2_PARCELA, E2_VENCREA, E2_VALOR, IIF(E2_BAIXA = '', CASE WHEN ISNUMERIC('R$ 0,00') = 0.00 THEN CAST(CAST(0.00 AS MONEY) AS FLOAT) END, SUM(E5_VALOR)) AS VLR_PAGO, E2_BAIXA 
FROM SE2990 AS SE2  
INNER JOIN SE5990 AS SE5  
ON SE5.E5_FILIAL = '01' AND   
SE2.E2_NOMFOR = SE5.E5_BENEF AND  
SE2.E2_PREFIXO = SE5.E5_PREFIXO AND  
SE2.E2_PARCELA = SE5.E5_PARCELA AND  
SE2.E2_TIPO = SE5.E5_TIPO AND  
SE2.E2_FORNECE = SE5.E5_CLIFOR 
AND  SE5.D_E_L_E_T_ = ''  
INNER JOIN SF1990 AS SF1  
ON SF1.F1_FILIAL = '01' AND  
SE2.E2_NUM = SF1.F1_DOC AND  
SE2.E2_PREFIXO =  SF1.F1_SERIE AND
SE2.E2_FORNECE =  SF1.F1_FORNECE AND
SE2.E2_LOJA =  SF1.F1_LOJA AND
SE5.E5_LA = SF1.F1_NUMTRIB AND
SF1.D_E_L_E_T_ = ''  
WHERE SF1.F1_DOC = '056933' AND
SE5.E5_RECPAG = 'P' AND  SE2.E2_FILIAL = '01' AND
SE2.D_E_L_E_T_ = ''  
GROUP BY E2_PARCELA, E2_VENCREA, E2_VALOR, E2_BAIXA

